    private boolean hostAtivo(String host){
                try {
            InetAddress address = InetAddress.getByName(host);
            boolean isReachable = address.isReachable(5000); // 5000 milissegundos (5 segundos) de timeout

            if (isReachable) {
                System.out.println("O host " + host + " está ativo.");
                return true;
            } else {
                System.out.println("O host " + host + " não está ativo.");
                return false;
            }
        } catch (Exception e) {
            System.err.println("Erro ao verificar o host: " + e.getMessage());
            return false;
        }
    }
    
