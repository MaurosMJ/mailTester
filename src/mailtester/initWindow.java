    private void jMenuItem2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenuItem2MouseClicked
        // TODO add your handling code here:
        // Obtém o diretório atual de onde o JAR está sendo executado
        String diretorioAtual = System.getProperty("user.dir");

        // Nome do arquivo de texto que você deseja criar e salvar
        String nomeArquivo = "arquivo.txt";

        // Caminho completo para o arquivo, incluindo o diretório atual
        String caminhoArquivo = diretorioAtual + "/" + nomeArquivo;

        try {
            // Cria um objeto BufferedWriter para escrever no arquivo
            BufferedWriter writer = new BufferedWriter(new FileWriter(caminhoArquivo));

            // Escreve algum conteúdo no arquivo de texto
            writer.write(logs_w);
            writer.newLine(); // Adiciona uma nova linha

            // Fecha o arquivo
            writer.close();

            System.out.println("Arquivo foi criado e salvo em: " + caminhoArquivo);
        } catch (IOException e) {
            e.printStackTrace();
            System.err.println("Erro ao criar e salvar o arquivo.");
        }
    }//GEN-LAST:event_jMenuItem2MouseClicked

